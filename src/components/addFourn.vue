    props:['visible'],
    watch:{
        visible(a){
            if(a){
                this.fourn = {}
                this.submitted = false
            }
        }
    },
    data(){
        return{
            fourn:{},
            isLoading:false,
            submitted:false
        }
    },
    methods:{
        async postFourn(){

            // console.log(this.fourn)
            this.isLoading = true
            this.submitted = true
            try {
                this.fourn.user_id = this.getUserId()
                const _r = await this.$http.post('api/fournisseurs',this.fourn)

                let _d = _r.data
                if(_d.status){
                    this.$emit('validate')
                    this.showNotif('success',`Ajout fournisseur`,`Fournisseur bien ajouté`)
                }else{
                    this.showNotif('error',`Ajout fournisseur`,_d.message)
                }
            } catch (e) {
                this.showNotifServerError()
            }

            this.isLoading = false
        },
    }
}
</script>

